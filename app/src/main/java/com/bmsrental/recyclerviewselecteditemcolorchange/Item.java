package com.bmsrental.recyclerviewselecteditemcolorchange;

public class Item {
    private String title;
    private String id;

    public Item() {
    }

    public Item(String title, String id) {
        this.title = title;
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public String getId() {
        return id;
    }
}
